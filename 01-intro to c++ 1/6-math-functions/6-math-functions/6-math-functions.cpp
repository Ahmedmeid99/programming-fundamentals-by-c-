#include <iostream>
#include <cmath>
using namespace std;

int main() {
	// math functions
	cout << "sqrt(16) = " << sqrt(16) << endl;         // 4
	cout << "round(12.3) = " << round(12.3) << endl;   // 12
	cout << "round(12.7) = " << round(12.7) << endl;   // 13
	cout << "pow(2,3) = " << pow(2,3) << endl;         // 8
	cout << "ceil(12.2) = " << ceil(12.2) << endl;     // 13
	cout << "ceil(12.9) = " << ceil(12.9) << endl;     // 13
	cout << "floor(12.2) = " << floor(12.2) << endl;   // 12
	cout << "floor(12.9) = " << floor(12.9) << endl;   // 12

	return 0;
}